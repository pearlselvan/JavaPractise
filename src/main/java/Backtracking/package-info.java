package Backtracking;

/*
Backtracking is a general algorithm for finding all (or some) solutions to some computational problems
, notably constraint satisfaction problems, that incrementally builds candidates
to the solutions, and abandons each partial candidate c ("backtracks") as soon as it determines that c cannot possibly be completed to a valid solution

Recur­sion is the key in back­track­ing pro­gram­ming.
 As the name sug­gests we back­track to find the solu­tion.
 We start with one pos­si­ble move out of many avail­able moves
  and try to solve the prob­lem if we are able to solve the
  prob­lem with the selected move then we will print the solu­tion
  else we will back­track and select some other move and try to solve it.
  If none if the moves work out we will claim that there is no solu­tion for the problem.

http://algorithms.tutorialhorizon.com/introduction-to-backtracking-programming/
 */
